            Contract::create([
                'contract_number'=>$request->contract_number,
                'id_user'=>$request->id_user,
                'id_field'=>$request->id_field,
                'id_skpd'=>$request->id_skpd,
            ]);
            $respon=[
                'status'=>'success',
                'msg'=>'Contract Make it',
            ];
        } else{
            $respon=[
                'status'=>'failed',
                'msg'=>'Number Contract Already Exist',
                'data'=>$contract->first()
            ];
            return response()->json($respon);
        }
    }
